const { getPosition } = require('./B');

describe('1607B. Odd Grasshopper', () => {
  it.each`
    n    | x0             | num            | result
    ${1} | ${0}           | ${1}           | ${-1}
    ${2} | ${0}           | ${2}           | ${1}
    ${3} | ${10}          | ${10}          | ${11}
    ${4} | ${10}          | ${99}          | ${110}
    ${5} | ${177}         | ${13}          | ${190}
    ${6} | ${10000000000} | ${987654321}   | ${9012345679}
    ${7} | ${-433494437}  | ${87178291199} | ${-87611785637}
    ${8} | ${1}           | ${0}           | ${1}
    ${9} | ${-1}          | ${1}           | ${0}
  `('Base test: $n', ({ x0, num, result }) => {
    expect(getPosition(x0, num)).toBe(result);
  });
});
