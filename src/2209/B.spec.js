const { getMaxCounts } = require('./B');

describe('2209A. Flip Flops', () => {
  it.each`
    n    | arr                                                                      | result
    ${1} | ${[1092]}                                                                | ${[0]}
    ${2} | ${[105, -105]}                                                           | ${[1, 0]}
    ${3} | ${[1, 2, 93, 84, 2]}                                                     | ${[4, 2, 2, 1, 0]}
    ${4} | ${[2, 9, 38, 4, 7, 1, 6]}                                                | ${[5, 4, 4, 2, 2, 1, 0]}
    ${5} | ${[1, 9, 20, 9, 829, 3, 87, 1, 283, 7]}                                  | ${[8, 4, 4, 3, 5, 3, 2, 2, 1, 0]}
    ${6} | ${[9, 18, 29817, 283, 3, 3928, 5726, 1942, 1000000000, -1000000000, 19]} | ${[8, 7, 7, 4, 5, 3, 3, 2, 2, 1, 0]}
  `('Base test: $n', ({ arr, result }) => {
    expect(getMaxCounts(arr)).toStrictEqual(result);
  });
});
