const { getMaxPower } = require('./A');

describe('2209A. Flip Flops', () => {
  it.each`
    n     | c             | k             | arr                                         | result
    ${1}  | ${12}         | ${23}         | ${[21]}                                     | ${12}
    ${2}  | ${8}          | ${4}          | ${[5]}                                      | ${16}
    ${3}  | ${3}          | ${4}          | ${[16]}                                     | ${3}
    ${4}  | ${6}          | ${3}          | ${[14, 9, 11]}                              | ${6}
    ${5}  | ${9}          | ${11}         | ${[20, 16, 18, 16, 11]}                     | ${9}
    ${6}  | ${18}         | ${30}         | ${[1, 2, 93, 84, 2]}                        | ${53}
    ${7}  | ${29}         | ${13}         | ${[2, 9, 38, 4, 7, 1, 6]}                   | ${109}
    ${8}  | ${9}          | ${2}          | ${[8, 1, 8, 11, 17, 3, 14, 16, 20, 10]}     | ${119}
    ${9}  | ${192}        | ${109}        | ${[1, 9, 20, 9, 829, 3, 87, 1, 283, 7]}     | ${721}
    ${10} | ${1000000000} | ${1000000000} | ${[19, 1000000000, 1, 9, 2, 3, 8, 1, 2, 3]} | ${3000000048}
  `('Base test: $n', ({ c, k, arr, result }) => {
    expect(getMaxPower(c, k, arr)).toBe(result);
  });
});
