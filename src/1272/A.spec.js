const { getMinDistance } = require('./A');

describe('1272A. Three Friends', () => {
  it.each`
    n    | a     | b             | c             | result
    ${1} | ${3}  | ${3}          | ${4}          | ${0}
    ${2} | ${10} | ${20}         | ${30}         | ${36}
    ${3} | ${5}  | ${5}          | ${5}          | ${0}
    ${4} | ${2}  | ${4}          | ${3}          | ${0}
    ${5} | ${1}  | ${1000000000} | ${1000000000} | ${1999999994}
    ${6} | ${1}  | ${1000000000} | ${999999999}  | ${1999999994}
    ${7} | ${3}  | ${2}          | ${5}          | ${2}
    ${8} | ${3}  | ${2}          | ${6}          | ${4}
  `('Base test: $n', ({ a, b, c, result }) => {
    expect(getMinDistance(a, b, c)).toBe(result);
  });
});
