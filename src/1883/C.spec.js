const { getMinCountOfOperations } = require('./C');

describe('1883C. Raspberries', () => {
  it.each`
    n     | arr                      | k    | result
    ${1}  | ${[7, 3]}                | ${5} | ${2}
    ${2}  | ${[7, 4, 1]}             | ${3} | ${2}
    ${3}  | ${[9, 7, 7, 3, 9]}       | ${2} | ${1}
    ${4}  | ${[5, 4, 1, 2, 3]}       | ${5} | ${0}
    ${5}  | ${[9, 5, 1, 5, 9, 5, 1]} | ${4} | ${2}
    ${6}  | ${[6, 3, 6]}             | ${4} | ${0}
    ${7}  | ${[6, 1, 5]}             | ${4} | ${1}
    ${8}  | ${[1, 5, 9]}             | ${4} | ${2}
    ${9}  | ${[1, 4, 1, 1]}          | ${4} | ${0}
    ${10} | ${[3, 5, 3]}             | ${4} | ${1}
    ${11} | ${[8, 9, 9, 3]}          | ${5} | ${1}
    ${12} | ${[1, 6]}                | ${5} | ${4}
    ${13} | ${[10, 10]}              | ${5} | ${0}
    ${14} | ${[1, 6, 1, 1]}          | ${5} | ${4}
    ${15} | ${[7, 7]}                | ${5} | ${3}
  `('Base test: $n', ({ arr, k, result }) => {
    expect(getMinCountOfOperations(arr, k)).toBe(result);
  });
});
