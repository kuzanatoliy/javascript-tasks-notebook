const { isItPossibleToWin } = require('./B');

describe('1480B. The Great Hero', () => {
  it.each`
    n    | A         | B         | arrA                              | arrB                              | result
    ${1} | ${3}      | ${17}     | ${[2]}                            | ${[16]}                           | ${'YES'}
    ${2} | ${10}     | ${999}    | ${[10, 20, 30]}                   | ${[100, 50, 30]}                  | ${'YES'}
    ${3} | ${1000}   | ${1000}   | ${[200, 300, 400, 500]}           | ${[1000, 1000, 1000, 1000]}       | ${'YES'}
    ${4} | ${999}    | ${999}    | ${[1000]}                         | ${[1000]}                         | ${'NO'}
    ${5} | ${999}    | ${999}    | ${[1000000]}                      | ${[999]}                          | ${'YES'}
    ${6} | ${775825} | ${720705} | ${[7766, 106800, 288507, 650107]} | ${[974721, 671951, 36538, 19204]} | ${'YES'}
  `('Base test: $n', ({ A, B, arrA, arrB, result }) => {
    expect(isItPossibleToWin(A, B, arrA, arrB)).toBe(result);
  });
});
