const { isIntersection } = require('./C');

describe('1971C. Clock and Strings', () => {
  it.each`
    n     | a     | b     | c     | d     | result
    ${1}  | ${2}  | ${9}  | ${10} | ${6}  | ${'YES'}
    ${2}  | ${3}  | ${8}  | ${9}  | ${1}  | ${'NO'}
    ${3}  | ${1}  | ${2}  | ${3}  | ${4}  | ${'NO'}
    ${4}  | ${5}  | ${3}  | ${4}  | ${12} | ${'YES'}
    ${5}  | ${1}  | ${8}  | ${2}  | ${10} | ${'YES'}
    ${6}  | ${3}  | ${12} | ${11} | ${8}  | ${'NO'}
    ${7}  | ${9}  | ${10} | ${12} | ${1}  | ${'NO'}
    ${8}  | ${12} | ${1}  | ${10} | ${2}  | ${'NO'}
    ${9}  | ${3}  | ${12} | ${6}  | ${9}  | ${'NO'}
    ${10} | ${1}  | ${9}  | ${8}  | ${4}  | ${'NO'}
    ${11} | ${6}  | ${7}  | ${9}  | ${12} | ${'NO'}
    ${12} | ${7}  | ${12} | ${9}  | ${6}  | ${'YES'}
    ${13} | ${10} | ${12} | ${11} | ${1}  | ${'YES'}
    ${14} | ${3}  | ${9}  | ${6}  | ${12} | ${'YES'}
    ${15} | ${1}  | ${4}  | ${3}  | ${5}  | ${'YES'}
  `('Base test: $n', ({ a, b, c, d, result }) => {
    expect(isIntersection(a, b, c, d)).toStrictEqual(result);
  });
});
