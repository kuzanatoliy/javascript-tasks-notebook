const { getMaxSum } = require('./B');

describe('1917B. Maximum Sumr', () => {
  it.each`
    n    | k    | arr                                                        | result
    ${1} | ${1} | ${[2, 5, 1, 10, 6]}                                        | ${21}
    ${2} | ${2} | ${[2, 5, 1, 10, 6]}                                        | ${11}
    ${3} | ${1} | ${[1, 2, 3]}                                               | ${3}
    ${4} | ${1} | ${[15, 22, 12, 10, 13, 11]}                                | ${62}
    ${5} | ${2} | ${[15, 22, 12, 10, 13, 11]}                                | ${46}
    ${6} | ${1} | ${[999999996, 999999999, 999999997, 999999998, 999999995]} | ${3999999986}
  `('Base test: $n', ({ k, arr, result }) => {
    expect(getMaxSum(k, arr)).toBe(result);
  });
});
