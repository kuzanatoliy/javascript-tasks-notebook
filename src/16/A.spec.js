const { isMeetStandart } = require('./A');

describe('16A. Flag', () => {
  it.each`
    n    | matrix                                                                                                                                     | result
    ${1} | ${[['0', '0', '0'], ['1', '1', '1'], ['2', '2', '2']]}                                                                                     | ${'YES'}
    ${2} | ${[['0', '0', '0'], ['0', '0', '0'], ['1', '1', '1']]}                                                                                     | ${'NO'}
    ${3} | ${[['0', '0', '0'], ['1', '1', '1'], ['0', '0', '2']]}                                                                                     | ${'NO'}
    ${4} | ${[['8', '8', '8', '8', '8'], ['4', '4', '4', '4', '4'], ['6', '6', '6', '6', '6'], ['5', '5', '5', '5', '5'], ['8', '8', '8', '8', '8']]} | ${'YES'}
  `('Base test: $n', ({ matrix, result }) => {
    expect(isMeetStandart(matrix)).toBe(result);
  });
});
