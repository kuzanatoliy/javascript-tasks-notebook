const { isItPossibleToTransform } = require('./A');

describe('1704A. Two 0-1 Sequences', () => {
  it.each`
    n     | str1          | str2        | result
    ${1}  | ${'001001'}   | ${'11'}     | ${'YES'}
    ${2}  | ${'110111'}   | ${'01'}     | ${'YES'}
    ${3}  | ${'000001'}   | ${'11'}     | ${'NO'}
    ${4}  | ${'111111'}   | ${'01'}     | ${'NO'}
    ${5}  | ${'10000101'} | ${'11010'}  | ${'NO'}
    ${6}  | ${'1010001'}  | ${'1001'}   | ${'YES'}
    ${7}  | ${'01010010'} | ${'010010'} | ${'YES'}
    ${8}  | ${'01010101'} | ${'1001'}   | ${'NO'}
    ${9}  | ${'10101010'} | ${'0110'}   | ${'NO'}
    ${10} | ${'1011100'}  | ${'11100'}  | ${'YES'}
  `('Base test: $n', ({ str1, str2, result }) => {
    expect(isItPossibleToTransform(str1, str2)).toBe(result);
  });
});
