const { isItPossibleToSplit } = require('./B');

describe('1881B. Three Threadlets', () => {
  it.each`
    n     | a             | b             | c             | result
    ${1}  | ${1}          | ${3}          | ${2}          | ${'YES'}
    ${2}  | ${5}          | ${5}          | ${5}          | ${'YES'}
    ${3}  | ${6}          | ${36}         | ${12}         | ${'NO'}
    ${4}  | ${7}          | ${8}          | ${7}          | ${'NO'}
    ${5}  | ${6}          | ${3}          | ${3}          | ${'YES'}
    ${6}  | ${4}          | ${4}          | ${12}         | ${'YES'}
    ${7}  | ${12}         | ${6}          | ${8}          | ${'NO'}
    ${8}  | ${1000000000} | ${1000000000} | ${1000000000} | ${'YES'}
    ${9}  | ${3}          | ${7}          | ${1}          | ${'NO'}
    ${10} | ${9}          | ${9}          | ${1}          | ${'NO'}
    ${11} | ${9}          | ${3}          | ${6}          | ${'YES'}
    ${12} | ${2}          | ${8}          | ${2}          | ${'YES'}
    ${13} | ${5}          | ${3}          | ${10}         | ${'NO'}
    ${14} | ${8}          | ${4}          | ${8}          | ${'YES'}
    ${15} | ${2}          | ${8}          | ${4}          | ${'NO'}
  `('Base test: $n', ({ a, b, c, result }) => {
    expect(isItPossibleToSplit(a, b, c)).toBe(result);
  });
});
