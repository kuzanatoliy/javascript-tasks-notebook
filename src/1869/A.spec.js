const { getOperations } = require('./A');

describe('1869A. Make It Zero', () => {
  it.each`
    n    | arr                         | result
    ${1} | ${[1, 2, 3, 0]}             | ${[[1, 4], [1, 4]]}
    ${2} | ${[3, 1, 4, 1, 5, 9, 2, 6]} | ${[[1, 8], [1, 8]]}
    ${3} | ${[1, 5, 4, 1, 4, 7]}       | ${[[1, 6], [1, 6]]}
    ${4} | ${[0, 0, 0, 0, 0]}          | ${[[1, 4], [1, 4], [4, 5], [4, 5]]}
    ${5} | ${[1, 1, 9, 9, 0, 1, 8]}    | ${[[1, 6], [1, 6], [6, 7], [6, 7]]}
    ${6} | ${[100, 100, 0]}            | ${[[1, 2], [1, 2], [2, 3], [2, 3]]}
  `('Base test: $n', ({ arr, result }) => {
    expect(getOperations(arr)).toStrictEqual(result);
  });
});
