const { getHeights } = require('./B');

describe('2232A. Convergence', () => {
  it.each`
    n    | arr                                  | result
    ${1} | ${[4, 2, 3]}                         | ${[4, 3, 3]}
    ${2} | ${[2, 3, 4, 3, 2]}                   | ${[2, 2, 2, 2, 2]}
    ${3} | ${[3, 3, 3, 1, 1]}                   | ${[3, 3, 3, 2, 2]}
    ${4} | ${[913764826, 346182673, 764382516]} | ${[913764826, 629973749, 629973749]}
    ${5} | ${[6, 7, 6, 7, 6, 7, 6, 7]}          | ${[6, 6, 6, 6, 6, 6, 6, 6]}
  `('Base test: $n', ({ arr, result }) => {
    expect(getHeights(arr)).toStrictEqual(result);
  });
});
