const { getMinAmount } = require('./A');

describe('2230A. Construct an Array', () => {
  it.each`
    n     | num          | a      | b      | result
    ${1}  | ${5}         | ${10}  | ${25}  | ${45}
    ${2}  | ${4}         | ${10}  | ${50}  | ${40}
    ${3}  | ${1}         | ${20}  | ${15}  | ${15}
    ${4}  | ${1}         | ${10}  | ${25}  | ${10}
    ${5}  | ${100000000} | ${100} | ${290} | ${9666666670}
    ${6}  | ${2}         | ${10}  | ${15}  | ${15}
    ${7}  | ${300}       | ${1}   | ${1}   | ${100}
    ${8}  | ${4}         | ${10}  | ${12}  | ${22}
    ${9}  | ${11}        | ${10}  | ${20}  | ${80}
    ${10} | ${12}        | ${10}  | ${15}  | ${60}
  `('Base test: $n', ({ num, a, b, result }) => {
    expect(getMinAmount(num, a, b)).toStrictEqual(result);
  });
});
