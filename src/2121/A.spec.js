const { getCountOfSteps } = require('./A');

describe('2121A. Letter Home', () => {
  it.each`
    n     | arr                     | s    | result
    ${1}  | ${[1]}                  | ${1} | ${0}
    ${2}  | ${[1]}                  | ${2} | ${1}
    ${3}  | ${[2]}                  | ${1} | ${1}
    ${4}  | ${[2, 3]}               | ${1} | ${2}
    ${5}  | ${[1, 3]}               | ${2} | ${3}
    ${6}  | ${[1, 2]}               | ${3} | ${2}
    ${7}  | ${[1, 2, 3]}            | ${1} | ${2}
    ${8}  | ${[1, 3, 4]}            | ${2} | ${4}
    ${9}  | ${[1, 2, 3]}            | ${3} | ${2}
    ${10} | ${[1, 2, 3, 10]}        | ${3} | ${11}
    ${11} | ${[1, 2, 3, 6, 7]}      | ${5} | ${8}
    ${12} | ${[1, 2, 3, 9, 10, 11]} | ${6} | ${15}
  `('Base test: $n', ({ arr, s, result }) => {
    expect(getCountOfSteps(arr, s)).toBe(result);
  });
});
