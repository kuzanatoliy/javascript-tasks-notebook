const { getMaxes } = require('./B');

describe('2104B. Move to the End', () => {
  it.each`
    n    | arr                                                                         | result
    ${1} | ${[13, 5, 10, 14, 8, 15, 13]}                                               | ${[15, 28, 42, 50, 63, 73, 78]}
    ${2} | ${[1000000000, 1000000000, 1000000000, 1000000000, 1000000000, 1000000000]} | ${[1000000000, 2000000000, 3000000000, 4000000000, 5000000000, 6000000000]}
    ${3} | ${[42]}                                                                     | ${[42]}
    ${4} | ${[7, 5]}                                                                   | ${[7, 12]}
  `('Base test: $n', ({ arr, result }) => {
    expect(getMaxes(arr)).toStrictEqual(result);
  });
});
