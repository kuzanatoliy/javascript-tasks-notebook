const { getMinCost } = require('./A');

describe('2119A. Add or XOR', () => {
  it.each`
    n    | a    | b      | x           | y           | result
    ${1} | ${1} | ${4}   | ${1}        | ${2}        | ${3}
    ${2} | ${1} | ${5}   | ${2}        | ${1}        | ${6}
    ${3} | ${3} | ${2}   | ${2}        | ${1}        | ${1}
    ${4} | ${1} | ${3}   | ${2}        | ${1}        | ${3}
    ${5} | ${2} | ${1}   | ${1}        | ${2}        | ${-1}
    ${6} | ${3} | ${1}   | ${1}        | ${2}        | ${-1}
    ${7} | ${1} | ${100} | ${10000000} | ${10000000} | ${990000000}
    ${8} | ${1} | ${2}   | ${9021863}  | ${2413763}  | ${9021863}
  `('Base test: $n', ({ a, b, x, y, result }) => {
    expect(getMinCost(a, b, x, y)).toBe(result);
  });
});
