const { isItPossibleToTransform } = require('./B');

describe('1050B. Tiling Challenge', () => {
  it.each`
    n    | matrix                                                                                                                                     | result
    ${1} | ${[['#', '.', '#'], ['.', '.', '.'], ['#', '.', '#']]}                                                                                     | ${'YES'}
    ${2} | ${[['#', '.', '#', '#'], ['#', '.', '.', '.'], ['#', '#', '#', '#'], ['#', '#', '.', '#']]}                                                | ${'NO'}
    ${3} | ${[['#', '.', '#', '#', '#'], ['.', '.', '.', '.', '#'], ['#', '.', '.', '.', '.'], ['#', '#', '#', '.', '#'], ['#', '#', '#', '#', '#']]} | ${'YES'}
    ${4} | ${(['#', '.', '#', '#', '#'], ['.', '.', '.', '.', '#'], ['#', '.', '.', '.', '.'], ['.', '.', '.', '.', '#'], ['#', '.', '.', '#', '#'])} | ${'NO'}
  `('Base test: $n', ({ matrix, result }) => {
    expect(isItPossibleToTransform(matrix)).toBe(result);
  });
});
