const { sortArray } = require('./C');

describe('2167B. Your Name', () => {
  it.each`
    n    | arr                | result
    ${1} | ${[2, 3, 1, 4]}    | ${[1, 2, 3, 4]}
    ${2} | ${[3, 2, 1, 3, 4]} | ${[1, 2, 3, 3, 4]}
    ${3} | ${[3, 7, 5, 1]}    | ${[3, 7, 5, 1]}
    ${4} | ${[1000000000, 2]} | ${[1000000000, 2]}
    ${5} | ${[1, 3, 5]}       | ${[1, 3, 5]}
    ${6} | ${[2, 5, 3, 1, 7]} | ${[1, 2, 3, 5, 7]}
    ${7} | ${[2, 4, 8, 6]}    | ${[2, 4, 8, 6]}
  `('Base test: $n', ({ arr, result }) => {
    expect(sortArray(arr)).toStrictEqual(result);
  });
});
