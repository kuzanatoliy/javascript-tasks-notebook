const { isSquare } = require('./A');

describe('2167A. Square?', () => {
  it.each`
    n    | a    | b     | c    | d    | result
    ${1} | ${1} | ${2}  | ${3} | ${4} | ${'NO'}
    ${2} | ${1} | ${1}  | ${1} | ${1} | ${'YES'}
    ${3} | ${2} | ${2}  | ${2} | ${2} | ${'YES'}
    ${4} | ${1} | ${2}  | ${1} | ${2} | ${'NO'}
    ${5} | ${1} | ${1}  | ${5} | ${5} | ${'NO'}
    ${6} | ${5} | ${5}  | ${5} | ${5} | ${'YES'}
    ${7} | ${4} | ${10} | ${5} | ${9} | ${'NO'}
  `('Base test: $n', ({ a, b, c, d, result }) => {
    expect(isSquare(a, b, c, d)).toBe(result);
  });
});
