const { getOperationCount } = require('./B');

describe('2337B. Annoying the Ghost', () => {
  it.each`
    n     | arrA                     | arrB                     | result
    ${1}  | ${[1, 2, 2]}             | ${[1, 3, 5]}             | ${0}
    ${2}  | ${[2, 2, 1]}             | ${[1, 2, 3]}             | ${2}
    ${3}  | ${[5, 1]}                | ${[2, 4]}                | ${-1}
    ${4}  | ${[6, 5, 4, 3, 2, 1]}    | ${[1, 2, 3, 4, 5, 6]}    | ${15}
    ${5}  | ${[4, 7, 1, 6, 2, 5, 3]} | ${[1, 2, 3, 4, 5, 6, 7]} | ${12}
    ${6}  | ${[2, 1]}                | ${[2, 3]}                | ${0}
    ${7}  | ${[3, 2, 2, 1]}          | ${[1, 2, 3, 4]}          | ${4}
    ${8}  | ${[4, 3, 2, 1]}          | ${[1, 3, 4, 5]}          | ${4}
    ${9}  | ${[1, 5, 4, 3, 2]}       | ${[2, 3, 4, 5, 6]}       | ${3}
    ${10} | ${[10, 3, 8, 6, 9]}      | ${[3, 6, 8, 9, 10]}      | ${5}
  `('Base test: $n', ({ arrA, arrB, result }) => {
    expect(getOperationCount(arrA, arrB)).toBe(result);
  });
});
