const { getMaxSum } = require('./C');

describe('2236C. Duck Surplus', () => {
  it.each`
    n     | arr                                                         | result
    ${1}  | ${[1, 3, 5]}                                                | ${5}
    ${2}  | ${[7, 3]}                                                   | ${10}
    ${3}  | ${[3, 2, 5, 1]}                                             | ${6}
    ${4}  | ${[2, 2, 1, 3, 3]}                                          | ${3}
    ${5}  | ${[3, 1, 4, 2]}                                             | ${6}
    ${6}  | ${[1, 4, 3, 2, 5]}                                          | ${14}
    ${7}  | ${[6, 2, 5, 1, 4, 3]}                                       | ${21}
    ${8}  | ${[2, 7, 1, 6, 3, 5, 4]}                                    | ${26}
    ${9}  | ${[8, 1, 7, 2, 6, 3, 5, 4]}                                 | ${36}
    ${10} | ${[1000000000, 999999999, 999999998, 999999997, 999999996]} | ${4999999990}
  `('Base test: $n', ({ arr, result }) => {
    expect(getMaxSum(arr)).toBe(result);
  });
});
