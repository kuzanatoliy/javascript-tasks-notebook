const { getDistance } = require('./A');

describe('1781A. Parallel Projection', () => {
  it.each`
    n    | w     | d       | h     | a     | b     | f     | g      | result
    ${1} | ${55} | ${20}   | ${29} | ${23} | ${10} | ${18} | ${3}   | ${47}
    ${2} | ${20} | ${10}   | ${5}  | ${1}  | ${5}  | ${2}  | ${5}   | ${8}
    ${3} | ${15} | ${15}   | ${4}  | ${7}  | ${13} | ${10} | ${10}  | ${14}
    ${4} | ${2}  | ${1000} | ${2}  | ${1}  | ${1}  | ${1}  | ${999} | ${1002}
    ${5} | ${10} | ${4}    | ${10} | ${7}  | ${1}  | ${2}  | ${1}   | ${17}
  `('Base test: $n', ({ w, d, h, a, b, f, g, result }) => {
    expect(getDistance(w, d, h, a, b, f, g)).toBe(result);
  });
});
