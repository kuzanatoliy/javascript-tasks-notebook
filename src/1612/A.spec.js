const { getPointC } = require('./A');

describe('1612A. Distance', () => {
  it.each`
    n     | x     | y     | result
    ${1}  | ${49} | ${3}  | ${[24, 2]}
    ${2}  | ${2}  | ${50} | ${[1, 25]}
    ${3}  | ${13} | ${0}  | ${[-1, -1]}
    ${4}  | ${0}  | ${41} | ${[-1, -1]}
    ${5}  | ${42} | ${0}  | ${[21, 0]}
    ${6}  | ${0}  | ${36} | ${[0, 18]}
    ${7}  | ${13} | ${37} | ${[6, 19]}
    ${8}  | ${42} | ${16} | ${[21, 8]}
    ${9}  | ${42} | ${13} | ${[-1, -1]}
    ${10} | ${0}  | ${0}  | ${[0, 0]}
  `('Base test: $n', ({ x, y, result }) => {
    expect(getPointC(x, y)).toStrictEqual(result);
  });
});
