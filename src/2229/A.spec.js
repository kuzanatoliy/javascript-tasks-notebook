const { getOperationCount } = require('./A');

describe('2229A. Slimes on a Line', () => {
  it.each`
    n     | arr                           | result
    ${1}  | ${[1, 2, 3, 4, 5]}            | ${2}
    ${2}  | ${[3, 3, 3, 3, 3]}            | ${0}
    ${3}  | ${[5, 6, 7, 1, 2, 3]}         | ${3}
    ${4}  | ${[2, 5]}                     | ${2}
    ${5}  | ${[1, 3, 8, 7]}               | ${4}
    ${6}  | ${[6, 2, 1, 8]}               | ${4}
    ${7}  | ${[1, 3, 9]}                  | ${4}
    ${8}  | ${[1, 10, 1, 10, 10]}         | ${5}
    ${9}  | ${[10, 8, 5, 9, 1, 6, 9, 10]} | ${5}
    ${10} | ${[1, 1000]}                  | ${500}
  `('Base test: $n', ({ arr, result }) => {
    expect(getOperationCount(arr)).toBe(result);
  });
});
