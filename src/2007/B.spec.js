const { getMaxValues } = require('./B');

describe('2007B. Index and Maximum Value', () => {
  it.each`
    n    | arr                | queries                                                              | result
    ${1} | ${[1, 2, 3, 2, 1]} | ${[['+', 1, 3], ['-', 2, 3], ['+', 1, 2], ['+', 2, 4], ['-', 6, 8]]} | ${[4, 4, 4, 5, 5]}
    ${2} | ${[1, 3, 3, 4, 5]} | ${[['+', 1, 4], ['+', 2, 3], ['-', 4, 5], ['-', 3, 3], ['-', 2, 6]]} | ${[5, 5, 4, 4, 3]}
    ${3} | ${[1, 1, 1, 1, 1]} | ${[['+', 2, 3], ['-', 4, 5], ['+', 1, 6], ['-', 2, 5], ['+', 1, 8]]} | ${[1, 1, 2, 1, 2]}
    ${4} | ${[1]}             | ${[['-', 1, 1]]}                                                     | ${[0]}
    ${5} | ${[1000000000]}    | ${[['+', 1000000000, 1000000000]]}                                   | ${[1000000001]}
  `('Base test: $n', ({ arr, queries, result }) => {
    expect(getMaxValues(arr, queries)).toStrictEqual(result);
  });
});
