const { getMaxValue } = require('./B');

describe('2194B. Offshores', () => {
  it.each`
    n    | x     | y     | arr                         | result
    ${1} | ${5}  | ${4}  | ${[10, 9, 8, 7]}            | ${25}
    ${2} | ${13} | ${11} | ${[47, 52, 64, 13, 91]}     | ${229}
    ${3} | ${1}  | ${1}  | ${[1000, 1000]}             | ${2000}
    ${4} | ${15} | ${14} | ${[34, 43, 52]}             | ${113}
    ${5} | ${7}  | ${6}  | ${[15, 17, 14, 15, 12, 16]} | ${72}
    ${6} | ${15} | ${10} | ${[45, 44]}                 | ${74}
  `('Base test: $n', ({ x, y, arr, result }) => {
    expect(getMaxValue(x, y, arr)).toBe(result);
  });
});
