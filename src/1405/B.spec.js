const { getCost } = require('./B');

describe('1405B. RPG Protagonist', () => {
  it.each`
    n    | arr                                                                            | result
    ${1} | ${[-3, 5, -3, 1]}                                                              | ${3}
    ${2} | ${[1, -1]}                                                                     | ${0}
    ${3} | ${[-3, 2, -3, 4]}                                                              | ${4}
    ${4} | ${[-1, 1, 1, -1]}                                                              | ${1}
    ${5} | ${[-5, 7, -6, -4, 17, -13, 4]}                                                 | ${8}
    ${6} | ${[-1000000000, -1000000000, -1000000000, 1000000000, 1000000000, 1000000000]} | ${3000000000}
    ${7} | ${[0]}                                                                         | ${0}
  `('Base test: $n', ({ arr, result }) => {
    expect(getCost(arr)).toBe(result);
  });
});
