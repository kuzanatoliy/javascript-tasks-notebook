const { isItPossibleToClick } = require('./A');

describe('373A. Playing with Dice', () => {
  it.each`
    n    | map                                                                                         | k    | result
    ${1} | ${[['.', '1', '3', '5'], ['1', '2', '4', '7'], ['3', '4', '6', '8'], ['5', '7', '8', '9']]} | ${1} | ${'YES'}
    ${2} | ${[['.', '.', '1', '.'], ['1', '1', '1', '1'], ['.', '.', '1', '.'], ['.', '.', '1', '.']]} | ${5} | ${'YES'}
    ${2} | ${[['.', '.', '.', '.'], ['1', '2', '.', '1'], ['.', '2', '.', '.'], ['.', '2', '.', '.']]} | ${1} | ${'NO'}
    ${2} | ${[['9', '9', '9', '9'], ['9', '9', '9', '9'], ['9', '9', '9', '9'], ['9', '9', '9', '9']]} | ${5} | ${'NO'}
  `('Base test: $n', ({ map, k, result }) => {
    expect(isItPossibleToClick(map, k)).toBe(result);
  });
});
