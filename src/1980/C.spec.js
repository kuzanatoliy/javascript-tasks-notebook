const { isItPossibleToGetArrB } = require('./C');

describe('1980C. Sofia and the Lost Operations', () => {
  it.each`
    n    | arrA                           | arrB                                  | arrD                         | result
    ${1} | ${[1, 2, 1]}                   | ${[1, 3, 2]}                          | ${[1, 3, 1, 2]}              | ${'YES'}
    ${2} | ${[1, 2, 3, 5]}                | ${[2, 1, 3, 5]}                       | ${[2, 3]}                    | ${'NO'}
    ${3} | ${[7, 6, 1, 10, 10]}           | ${[3, 6, 1, 11, 11]}                  | ${[4, 3, 11]}                | ${'NO'}
    ${4} | ${[3, 1, 7, 8]}                | ${[2, 2, 7, 10]}                      | ${[10, 3, 2, 2, 1]}          | ${'NO'}
    ${5} | ${[5, 7, 1, 7, 9]}             | ${[4, 10, 1, 2, 9]}                   | ${[1, 1, 9, 8, 7, 2, 10, 4]} | ${'YES'}
    ${6} | ${[1000000000, 203, 203, 203]} | ${[203, 1000000000, 203, 1000000000]} | ${[203, 1000000000]}         | ${'NO'}
    ${7} | ${[1]}                         | ${[1]}                                | ${[1, 3, 4, 5, 1]}           | ${'YES'}
  `('BtasarrBsse test: $n', ({ arrA, arrB, arrD, result }) => {
    expect(isItPossibleToGetArrB(arrA, arrB, arrD)).toBe(result);
  });
});
