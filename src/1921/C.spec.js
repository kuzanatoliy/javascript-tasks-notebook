const { isItPossibleToSend } = require('./C');

describe('1921C. Sending Messages', () => {
  it.each`
    n    | f             | a             | b             | arr                                                                                             | result
    ${1} | ${3}          | ${1}          | ${5}          | ${[3]}                                                                                          | ${'NO'}
    ${2} | ${21}         | ${1}          | ${3}          | ${[4, 6, 10, 13, 17, 20, 26]}                                                                   | ${'YES'}
    ${3} | ${10}         | ${1}          | ${2}          | ${[1, 2, 3, 4, 5]}                                                                              | ${'YES'}
    ${4} | ${1000000000} | ${1000000000} | ${1000000000} | ${[1000000000]}                                                                                 | ${'NO'}
    ${5} | ${11}         | ${9}          | ${6}          | ${[6, 8, 10]}                                                                                   | ${'NO'}
    ${6} | ${621526648}  | ${2585904}    | ${3566299}    | ${[51789, 61859, 71998, 73401, 247675, 298086, 606959, 663464, 735972, 806043, 806459, 919683]} | ${'YES'}
  `('Base test: $n', ({ f, a, b, arr, result }) => {
    expect(isItPossibleToSend(f, a, b, arr)).toStrictEqual(result);
  });
});
