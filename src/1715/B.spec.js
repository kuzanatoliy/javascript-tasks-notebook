const { buildArray } = require('./B');

describe('1715B. Beautiful Array', () => {
  it.each`
    n    | num  | k             | b             | s                      | result
    ${1} | ${1} | ${6}          | ${3}          | ${100}                 | ${[-1]}
    ${2} | ${3} | ${6}          | ${3}          | ${12}                  | ${[-1]}
    ${3} | ${3} | ${6}          | ${3}          | ${19}                  | ${[19, 0, 0]}
    ${4} | ${5} | ${4}          | ${7}          | ${38}                  | ${[31, 3, 3, 1, 0]}
    ${5} | ${5} | ${4}          | ${7}          | ${80}                  | ${[-1]}
    ${6} | ${1} | ${1}          | ${0}          | ${0}                   | ${[0]}
    ${7} | ${4} | ${1000000000} | ${1000000000} | ${1000000000000000000} | ${[1000000000000000000, 0, 0, 0]}
  `('Base test: $n', ({ num, k, b, s, result }) => {
    expect(buildArray(num, k, b, s)).toStrictEqual(result);
  });
});
