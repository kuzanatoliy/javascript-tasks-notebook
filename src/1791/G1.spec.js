const { getTeleportsCount } = require('./G1');

describe('1791G1. Teleporters (Easy Version)', () => {
  it.each`
    n     | c            | arr                                                        | result
    ${1}  | ${6}         | ${[1, 1, 1, 1, 1]}                                         | ${2}
    ${2}  | ${32}        | ${[100, 52, 13, 6, 9, 4, 100, 35]}                         | ${2}
    ${3}  | ${1}         | ${[5]}                                                     | ${0}
    ${4}  | ${5}         | ${[4, 3, 2, 1]}                                            | ${1}
    ${5}  | ${9}         | ${[2, 3, 1, 4, 1]}                                         | ${2}
    ${6}  | ${8}         | ${[2, 3, 1, 4, 1]}                                         | ${2}
    ${7}  | ${3}         | ${[2, 3, 4, 1]}                                            | ${1}
    ${8}  | ${9}         | ${[5, 4, 3, 3]}                                            | ${1}
    ${9}  | ${14}        | ${[7, 5]}                                                  | ${1}
    ${10} | ${600000000} | ${[500000000, 400000000, 300000000, 200000000, 100000000]} | ${2}
  `('Base test: $n', ({ c, arr, result }) => {
    expect(getTeleportsCount(c, arr)).toBe(result);
  });
});
