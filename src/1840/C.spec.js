const { getCountOfWays } = require('./C');

describe('1840C. Ski Resort', () => {
  it.each`
    n    | arr                      | k    | q      | result
    ${1} | ${[-5, 0, -10]}          | ${1} | ${15}  | ${6}
    ${2} | ${[8, 12, 9, 0, 5]}      | ${3} | ${-33} | ${0}
    ${3} | ${[12, 12, 10, 15]}      | ${3} | ${12}  | ${1}
    ${4} | ${[0, -1, 2, 5]}         | ${1} | ${-5}  | ${0}
    ${5} | ${[3, -1, 4, -5, -3]}    | ${5} | ${0}   | ${0}
    ${6} | ${[5]}                   | ${1} | ${5}   | ${1}
    ${7} | ${[0, 3, -2, 5, -4, -4]} | ${1} | ${3}   | ${9}
  `('Base test: $n', ({ arr, k, q, result }) => {
    expect(getCountOfWays(arr, k, q)).toBe(result);
  });
});
