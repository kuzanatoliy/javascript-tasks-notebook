const { getPositions } = require('./B');

describe('1537B. Bad Boy', () => {
  it.each`
    n    | num           | m             | x             | y     | result
    ${1} | ${2}          | ${3}          | ${1}          | ${1}  | ${[2, 1, 1, 3]}
    ${2} | ${4}          | ${4}          | ${1}          | ${2}  | ${[4, 1, 1, 4]}
    ${3} | ${3}          | ${5}          | ${2}          | ${2}  | ${[3, 1, 1, 5]}
    ${4} | ${5}          | ${1}          | ${2}          | ${1}  | ${[5, 1, 1, 1]}
    ${5} | ${3}          | ${1}          | ${3}          | ${1}  | ${[3, 1, 1, 1]}
    ${6} | ${1}          | ${1}          | ${1}          | ${1}  | ${[1, 1, 1, 1]}
    ${7} | ${1000000000} | ${1000000000} | ${1000000000} | ${50} | ${[1000000000, 1, 1, 1000000000]}
  `('Base test: $n', ({ num, m, x, y, result }) => {
    expect(getPositions(num, m, x, y)).toStrictEqual(result);
  });
});
