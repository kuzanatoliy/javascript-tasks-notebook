const { getTeleportsCount } = require('./B');

describe('1899A. Game with Integers', () => {
  it.each`
    n    | arr                                                                                                      | result
    ${1} | ${[1, 2]}                                                                                                | ${1}
    ${2} | ${[10, 2, 3, 6, 1, 3]}                                                                                   | ${9}
    ${3} | ${[1000000000, 1000000000, 1000000000, 1000000000]}                                                      | ${0}
    ${4} | ${[60978, 82265, 78961, 56708, 39846, 31071, 4913, 4769, 29092, 91348, 64119, 72421, 98405, 222, 14294]} | ${189114}
    ${5} | ${[19957, 69913, 37531, 96991, 57838, 21008, 14207, 19198]}                                              | ${112141}
  `('Base test: $n', ({ arr, result }) => {
    expect(getTeleportsCount(arr)).toBe(result);
  });
});
