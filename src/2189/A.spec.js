const { getMaxDiff } = require('./A');

describe('2184A. Table with Numbers', () => {
  it.each`
    n    | h    | l    | arr                         | result
    ${1} | ${1} | ${1} | ${[1, 1]}                   | ${1}
    ${2} | ${2} | ${2} | ${[1, 2, 2, 3, 2]}          | ${2}
    ${3} | ${4} | ${2} | ${[7, 2, 2, 2, 3, 4, 4, 2]} | ${3}
    ${4} | ${3} | ${6} | ${[10, 4, 1, 3, 5, 4, 6]}   | ${2}
    ${5} | ${4} | ${4} | ${[5, 5]}                   | ${0}
    ${6} | ${6} | ${3} | ${[10, 4, 1, 3, 5, 4, 6]}   | ${2}
    ${7} | ${1} | ${1} | ${[1, 1, 1, 1]}             | ${2}
    ${8} | ${1} | ${1} | ${[1, 2]}                   | ${0}
  `('Base test: $n', ({ num, result }) => {
    expect(getMaxDiff(num)).toBe(result);
  });
});
