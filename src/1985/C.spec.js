const { getCountOfGoodPrefixes } = require('./C');

describe('1985C. Good Prefixes', () => {
  it.each`
    n    | arr                                                                                                               | result
    ${1} | ${[0]}                                                                                                            | ${1}
    ${2} | ${[1]}                                                                                                            | ${0}
    ${3} | ${[1, 1, 2, 0]}                                                                                                   | ${3}
    ${4} | ${[0, 1, 2, 1, 4]}                                                                                                | ${3}
    ${5} | ${[1, 1, 0, 3, 5, 2, 12]}                                                                                         | ${4}
    ${6} | ${[1000000000, 1000000000, 1000000000, 1000000000, 1000000000, 1000000000, 294967296]}                            | ${1}
    ${7} | ${[0, 1000000000, 1000000000, 1000000000, 1000000000, 1000000000, 1000000000, 1000000000, 1000000000, 589934592]} | ${2}
  `('Base test: $n', ({ arr, result }) => {
    expect(getCountOfGoodPrefixes(arr)).toBe(result);
  });
});
