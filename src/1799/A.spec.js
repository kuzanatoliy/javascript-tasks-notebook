const { getMap } = require('./A');

describe('1799A. Recent Actions', () => {
  it.each`
    n     | num  | m     | p                                                                            | result
    ${1}  | ${1} | ${1}  | ${[2]}                                                                       | ${[1]}
    ${2}  | ${3} | ${2}  | ${[5, 4]}                                                                    | ${[-1, 2, 1]}
    ${3}  | ${4} | ${5}  | ${[5, 9, 9, 5, 7]}                                                           | ${[-1, 5, 2, 1]}
    ${4}  | ${5} | ${5}  | ${[6, 7, 8, 9, 10]}                                                          | ${[5, 4, 3, 2, 1]}
    ${5}  | ${3} | ${4}  | ${[4, 4, 4, 4]}                                                              | ${[-1, -1, 1]}
    ${6}  | ${4} | ${4}  | ${[5, 5, 6, 6]}                                                              | ${[-1, -1, 3, 1]}
    ${7}  | ${3} | ${5}  | ${[4, 5, 5, 5, 4]}                                                           | ${[-1, 2, 1]}
    ${8}  | ${4} | ${20} | ${[5, 5, 24, 24, 24, 5, 6, 7, 8, 9, 10, 12, 13, 14, 15, 16, 17, 18, 19, 20]} | ${[8, 7, 3, 1]}
    ${9}  | ${5} | ${7}  | ${[7, 8, 7, 11, 7, 12, 10]}                                                  | ${[7, 6, 4, 2, 1]}
    ${10} | ${6} | ${7}  | ${[8, 11, 7, 8, 8, 8, 12]}                                                   | ${[-1, -1, 7, 3, 2, 1]}
  `('Base test: $n', ({ num, m, p, result }) => {
    expect(getMap(num, m, p)).toStrictEqual(result);
  });
});
